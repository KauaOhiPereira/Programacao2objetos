package concreto;

public class Sabor {
	public void salgado() {
		System.out.println("Muito salgado!");
	}
	public void doce() {
		System.out.println("Até que é bom.");
	}
	public void azedo() {
		System.out.println("Muito azedo!");
	}
	public void amargo() {
		System.out.println("Não é para meu gosto.");
	}
	public void desconhecido() {
		System.out.println("Não conheço este gosto.");
	}
}
